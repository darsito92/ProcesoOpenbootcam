let nota =5;

switch (nota){
    case 5:
        console.log("enorabuena, has obtenido sobresaliente"); 
        break;
    case 4:
        console.log("buen trabajo, aun puedes mejorar");
        break;
    case 3:
        console.log("has obtenido un suficiente"); 
        break;
    case 2:
        console.log("no has aprobado por poco"); 
        break;
    case 1:
        console.log("se nota que no has estudiado debes mejorar"); 
        break;
    default:
        console.log("error"); 
        break;
}
